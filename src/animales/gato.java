public class gato {
    String colaGato;
    String cabezaGato;
    String orejasGato;

    public String getColaGato() {
        return colaGato;
    }

    public void setColaGato(String colaGato) {
        this.colaGato = colaGato;
    }

    public String getCabezaGato() {
        return cabezaGato;
    }

    public void setCabezaGato(String cabezaGato) {
        this.cabezaGato = cabezaGato;
    }

    public String getOrejasGato() {
        return orejasGato;
    }

    public void setOrejasGato(String orejasGato) {
        this.orejasGato = orejasGato;
    }

    public gato(String colaGato, String cabezaGato, String orejasGato) {
        this.colaGato = colaGato;
        this.cabezaGato = cabezaGato;
        this.orejasGato = orejasGato;
    }
    
}
